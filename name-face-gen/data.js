// ---------- Generation logic ----------

const pick = (arr) => arr[Math.floor(Math.random() * arr.length)];
const roll = () => Math.random();

// Clean up triple consonants and awkward letter pairs from fusion
function cleanFusion(s) {
  // collapse 3+ same consonants in a row to 2
  s = s.replace(/([bcdfghjklmnpqrstvwxz])\1{2,}/gi, "$1$1");
  // collapse some awkward triple-consonant clusters
  s = s.replace(/ttb/gi, "tb").replace(/ckk/gi, "ck").replace(/ssk/gi, "sk");
  s = s.replace(/kkk/gi, "k").replace(/sss/gi, "ss");
  return s;
}

function buildSurname(D) {
  // 50/50: full static last name, OR fusion prefix as the "root"
  const useStatic = roll() < 0.5;
  let root = useStatic ? pick(D.static_last_names) : pick(D.fusion_prefixes);
  // If we used a fusion prefix, we need at least one suffix to make it a "name"
  if (!useStatic) {
    root = cleanFusion(root + pick(D.fusion_suffixes));
  }
  return root;
}

// A "short" surname half — for hyphenated names so they don't get insanely long.
// Prefers shorter static names and small prefix+suffix combos.
function buildShortSurnameHalf(D) {
  const useStatic = roll() < 0.5;
  if (useStatic) {
    // Pick from shorter static last names only
    const shortStatics = D.static_last_names.filter(n => n.length <= 8);
    return pick(shortStatics);
  }
  // Pick a short prefix + short suffix
  const shortPrefixes = D.fusion_prefixes.filter(p => p.length <= 5);
  const shortSuffixes = D.fusion_suffixes.filter(s => s.length <= 4);
  return cleanFusion(pick(shortPrefixes) + pick(shortSuffixes));
}

function maybeAddSuffix(name, D, prob) {
  if (roll() <= prob) {
    return cleanFusion(name + pick(D.fusion_suffixes));
  }
  return null;
}

function generateName(gender, forcedTier) {
  const D = window.NAME_DATA;
  const g = gender || pick(["male", "female", "neutral"]);
  const first = pick(D.first_names[g]);

  // Determine rarity by walking the algorithm.
  // Algorithm probabilities from spec:
  //  - Vanilla: 35%  -> Common
  //  - Suffixed: 32.5% -> Uncommon (per spec it'd be Uncommon/Rare visual)
  //  - Hyphenated: 29.25% -> Rare/Epic
  //  - Double-suffixed: 3.25% -> Legendary (or divine root)
  // We'll map to the "Loot Drop Visual Tiers":
  //   Common 50%, Uncommon 30%, Rare 14%, Epic 5%, Legendary 1%
  // Use weighted roll to feel right.
  const r = roll();
  let tier;
  // Tier thresholds — adjust here to change drop rates.
  // common 50%, uncommon 30%, rare 14%, epic 4.5%, legendary 1%, mythic 0.5%
  if (r < 0.50) tier = "common";
  else if (r < 0.80) tier = "uncommon";
  else if (r < 0.94) tier = "rare";
  else if (r < 0.985) tier = "epic";
  else if (r < 0.995) tier = "legendary";
  else tier = "ultra"; // MYTHIC — 1 in 200

  // Debug override: if a tier is forced (e.g. from the dev panel), use it
  if (forcedTier) tier = forcedTier;

  let displayName, fullData = { tier, gender: g, first };

  if (tier === "ultra") {
    // MYTHIC: corporate title + divine root + ", " + epic title  (e.g. "Executive VP Kratos, The Doom-Slayer")
    const root = pick(D.legendary_roots[g]);
    const title = pick(D.legendary_titles);
    const corp = pick(D.corporate_titles);
    displayName = `${corp} ${root}, ${title}`;
    fullData.legendaryStyle = "ultra";
    fullData.legendaryRoot = root;
    fullData.legendaryTitle = title;
    fullData.corpTitle = corp;
  } else if (tier === "legendary") {
    // LEGENDARY: corporate title + divine first + random surname.
    // 50% pure divine ("Kratos The Doom-Slayer"), 50% hybrid (corp title + divine first + random surname, occasionally hyphen)
    const path = roll() < 0.5 ? "divine" : "hybrid";
    const root = pick(D.legendary_roots[g]);
    if (path === "divine") {
      const title = pick(D.legendary_titles);
      displayName = `${root} ${title}`;
      fullData.legendaryStyle = "divine";
      fullData.legendaryRoot = root;
      fullData.legendaryTitle = title;
    } else {
      const corp = pick(D.corporate_titles);
      // 25% hyphen flourish, otherwise single short surname
      const sur = roll() < 0.25
        ? `${buildShortSurnameHalf(D)}-${buildShortSurnameHalf(D)}`
        : buildShortSurnameHalf(D);
      displayName = `${corp} ${root} ${sur}`;
      fullData.legendaryStyle = "hybrid";
      fullData.legendaryRoot = root;
      fullData.corpTitle = corp;
      fullData.surname = sur;
    }
  } else if (tier === "epic") {
    // EPIC: ALWAYS gets a corporate title. Hyphen is a 30% flourish.
    const corp = pick(D.corporate_titles);
    const sur = roll() < 0.30
      ? `${buildShortSurnameHalf(D)}-${buildShortSurnameHalf(D)}`
      : buildShortSurnameHalf(D);
    displayName = `${corp} ${first} ${sur}`;
    fullData.corpTitle = corp;
    fullData.surname = sur;
  } else if (tier === "rare") {
    // RARE: suffixed surname, occasional (15%) hyphen flourish, no title
    const sur1 = cleanFusion(buildSurname(D) + pick(D.fusion_suffixes));
    if (roll() < 0.15) {
      const sur2 = buildShortSurnameHalf(D);
      displayName = `${first} ${sur1}-${sur2}`;
      fullData.surname = `${sur1}-${sur2}`;
    } else {
      displayName = `${first} ${sur1}`;
      fullData.surname = sur1;
    }
  } else if (tier === "uncommon") {
    // Static first + fused name
    const sur = cleanFusion(pick(D.fusion_prefixes) + pick(D.fusion_suffixes));
    displayName = `${first} ${sur}`;
    fullData.surname = sur;
  } else {
    // common: vanilla
    const sur = pick(D.static_last_names);
    displayName = `${first} ${sur}`;
    fullData.surname = sur;
  }

  fullData.displayName = displayName;
  return fullData;
}

window.generateName = generateName;
